# Ejercicio 2.7
# Escribe un programa que solicite tres números al usuario y calcule e imprima por pantalla su suma.


a = int(input("Introduce un número: "))                 #Pide un número y lo almacena en una variable.
b = int(input("Introduce otro número: "))               #Pide otro número y lo introduce en otra variable
c = int(input("Introduce otro número: "))               #Pide un último número y lo introduce en una variable.

z = a + b + c                                           #Suma las variables a, b y c y el resultado se almacena en z.

print("La suma total de los tres números es:", z)      #Imprime en pantalla el resultado de la suma de las tres variables.